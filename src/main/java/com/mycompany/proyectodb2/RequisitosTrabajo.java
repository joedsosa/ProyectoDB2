package com.mycompany.proyectodb2;

class RequisitosTrabajo {
    public String PuestosSI;
    public String PuestosNO;
    public String Disponibilidad;
    public String Contrato;
    public int Salario;

    public RequisitosTrabajo() {
    }

    public String getPuestosSI() {
        return PuestosSI;
    }

    public void setPuestosSI(String PuestosSI) {
        this.PuestosSI = PuestosSI;
    }

    public String getPuestosNO() {
        return PuestosNO;
    }

    public void setPuestosNO(String PuestosNO) {
        this.PuestosNO = PuestosNO;
    }

    public String getDisponibilidad() {
        return Disponibilidad;
    }

    public void setDisponibilidad(String Disponibilidad) {
        this.Disponibilidad = Disponibilidad;
    }

    public String getContrato() {
        return Contrato;
    }

    public void setContrato(String Contrato) {
        this.Contrato = Contrato;
    }

    public int getSalario() {
        return Salario;
    }

    public void setSalario(int Salario) {
        this.Salario = Salario;
    }
    
    
}
